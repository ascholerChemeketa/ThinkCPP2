#include <iostream>
using namespace std;

int main() {
    // store some character values
    char letter = 'a';
    char letter2 = 'A';
    char period = '.';
    char space = ' ';

    // print character variables
    cout << letter << endl;
    cout << letter2 << endl;
    cout << period << endl;
    cout << space << endl;

    // print a character literal
    cout << 'x' << endl;
}