#include <iostream>
using namespace std;

int main() {
    int x = 5;
    int y = 4;

    cout << "x == y " << (x == y) << endl; // x is equal to y
    cout << "x != y " << (x != y) << endl; // x is not equal to y
    cout << "x > y "  << (x > y)  << endl; // x is greater than y
    cout << "x < y "  << (x < y)  << endl; // x is less than y
    cout << "x >= y " << (x >= y) << endl; // x is greater than or equal to y
    cout << "x <= y " << (x <= y) << endl; // x is less than or equal to y
}

