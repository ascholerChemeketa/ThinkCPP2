#include <iostream>
using namespace std;

bool isOdd(int number) {
    bool result = false;
    if (number % 2 == 1) {
        result = true;
    } else {
        result = false;
    }
    return result;
}

int main() {
    cout << isOdd(3) << endl;
    cout << isOdd(4) << endl;
    cout << isOdd(15) << endl;
}