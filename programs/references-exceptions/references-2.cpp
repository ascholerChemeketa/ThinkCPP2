#include <iostream>
#include <vector>
using namespace std;

int main() {
    int a = 5;
    int& b = a;   // b is a reference to a
    b = 10;       // change a through b
    cout << "a: " << a << endl;
    cout << "b: " << b << endl;
    a = 6;        // change a directly
    cout << "a: " << a << endl;
    cout << "b: " << b << endl;
}