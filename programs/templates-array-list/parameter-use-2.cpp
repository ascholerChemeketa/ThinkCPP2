#include <iostream>
using namespace std;

template<typename T>
void printMultiple(T value, int n) {
    for (int i = 0; i < n; ++i) {
        cout << value;
    }
}

int main() {
    printMultiple(5, 3);        // prints "555"
    cout << endl;
    printMultiple("*", 10);     // prints "**********"
    cout << endl;
}